package goal.vo;

public class BoardFileVO {
	
	private int UUID;
	private int bno;
	private String i_filename;
	private String i_path;
	
	public int getUUID() {
		return UUID;
	}
	public void setUUID(int uUID) {
		this.UUID = uUID;
	}
	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public String getI_filename() {
		return i_filename;
	}
	public void setI_filename(String i_filename) {
		this.i_filename = i_filename;
	}
	public String getI_path() {
		return i_path;
	}
	public void setI_path(String i_path) {
		this.i_path = i_path;
	}
	
	
}
